package javabasics;

public class MathClass {
	public static void main(String[]args) {
		
		System.out.println("Mathematical operator");
		System.out.println("Maximum of (2,5) : "+ Math.max(2,5));
		System.out.println("Minimum of (2,5) : "+ Math.min(2,5));
		System.out.println("Absolute of (3.5) : "+ Math.abs(3.5));
		System.out.println("Exp of 10 : "+ Math.exp(10));
		System.out.println("Random number : "+ (int)Math.random()*50);
		System.out.println("Square root : "+ Math.sqrt(112));
	}
	

}
